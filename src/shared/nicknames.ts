const ADJECTIVES = [
  "Невідомий", "Пустельний", "Фіолетовий", "Лінивий", "Сонний", "Кмітливий", "Загадковий",
  "Сміливий", "Веселий", "Тихий", "Швидкий", "Дивний", "Мовчазний", "Крихітний", "Гучний",
  "Елегантний", "Кудлатий", "Замріяний", "Схвильований", "Непередбачуваний", "Хитрий",
  "Урочистий", "Меланхолійний"
];

const NOUNS = [
  "Жираф", "Орел", "Ананас", "Пінгвін", "Їжак", "Кіт", "Слон", "Кабан", "Тигр", "Бобер",
  "Фламінго", "Хом’як", "Дракон", "Метелик", "Восьминіг", "Панда", "Лев", "Вовк", "Сова",
  "Черепаха", "Тюлень", "Бізон"
];

function pick<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)];
}

/** Generates a random anonymous nickname, e.g. "Лінивий Пінгвін". */
export function generateNickname(): string {
  return `${pick(ADJECTIVES)} ${pick(NOUNS)}`;
}
